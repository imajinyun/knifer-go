package main

import (
	"fmt"
	"go/constant"
	"go/types"
	"os"
	"sort"
	"strings"

	"golang.org/x/tools/go/packages"
)

const modulePath = "github.com/imajinyun/knifer-go"

type apiSnapshotPackage struct {
	importPath string
	lines      []string
}

func main() {
	lines, err := generateSnapshot(".")
	if err != nil {
		fatal(err)
	}
	if _, err := fmt.Fprint(os.Stdout, strings.Join(lines, "\n")); err != nil {
		fatal(err)
	}
}

func generateSnapshot(root string) ([]string, error) {
	pkgs, err := loadPublicPackages(root)
	if err != nil {
		return nil, err
	}

	lines := []string{
		"# Exported API Snapshot",
		"# Generated by bin/check_api_compat.sh. Run UPDATE_API=1 make api-check after intentional public API changes.",
		"# Scope: module root and top-level v* facade packages only.",
		"# Detail: function signatures, exported type definitions, exported struct fields, interface methods, and method sets.",
		"",
	}
	for _, pkg := range pkgs {
		if len(pkg.lines) == 0 {
			continue
		}
		lines = append(lines, pkg.importPath)
		for _, line := range pkg.lines {
			lines = append(lines, "  "+line)
		}
		lines = append(lines, "")
	}
	return lines, nil
}

func loadPublicPackages(root string) ([]apiSnapshotPackage, error) {
	cfg := &packages.Config{
		Dir:  root,
		Mode: packages.NeedName | packages.NeedTypes | packages.NeedTypesInfo | packages.NeedImports | packages.NeedDeps,
	}
	pkgs, err := packages.Load(cfg, "./...")
	if err != nil {
		return nil, err
	}

	out := make([]apiSnapshotPackage, 0, len(pkgs))
	for _, pkg := range pkgs {
		if len(pkg.Errors) > 0 {
			return nil, pkg.Errors[0]
		}
		if pkg.Name == "main" || pkg.Types == nil || !isPublicAPIPackage(pkg.PkgPath) {
			continue
		}
		lines := exportedAPILines(pkg)
		if len(lines) == 0 {
			continue
		}
		out = append(out, apiSnapshotPackage{importPath: pkg.PkgPath, lines: lines})
	}
	sort.Slice(out, func(i, j int) bool { return out[i].importPath < out[j].importPath })
	return out, nil
}

func exportedAPILines(pkg *packages.Package) []string {
	scope := pkg.Types.Scope()
	qualifier := qualifierFor(pkg.Types)
	lines := make([]string, 0, scope.Len())
	for _, name := range scope.Names() {
		obj := scope.Lookup(name)
		if obj == nil || !obj.Exported() {
			continue
		}
		lines = append(lines, objectLines(obj, qualifier)...)
	}
	sort.Strings(lines)
	return lines
}

func objectLines(obj types.Object, qualifier types.Qualifier) []string {
	switch o := obj.(type) {
	case *types.Const:
		return []string{fmt.Sprintf("const %s %s = %s", o.Name(), typeString(o.Type(), qualifier), constantString(o.Val()))}
	case *types.Var:
		return []string{fmt.Sprintf("var %s %s", o.Name(), typeString(o.Type(), qualifier))}
	case *types.Func:
		return []string{fmt.Sprintf("func %s%s", o.Name(), signatureTail(o.Type().(*types.Signature), qualifier))}
	case *types.TypeName:
		return typeLines(o, qualifier)
	default:
		return nil
	}
}

func typeLines(obj *types.TypeName, qualifier types.Qualifier) []string {
	if obj.IsAlias() {
		return aliasTypeLines(obj, qualifier)
	}

	named, _ := obj.Type().(*types.Named)
	head := obj.Name()
	if named != nil {
		head += typeParamList(named.TypeParams(), qualifier)
	}

	lines := []string{definedTypeLine(head, obj.Type().Underlying(), qualifier)}
	if named != nil {
		lines = append(lines, methodLines(named, qualifier)...)
	}
	return lines
}

func aliasTypeLines(obj *types.TypeName, qualifier types.Qualifier) []string {
	head := obj.Name()
	target := obj.Type()
	if alias, ok := target.(*types.Alias); ok {
		head += typeParamList(alias.TypeParams(), qualifier)
		target = alias.Rhs()
	}
	return []string{fmt.Sprintf("type %s = %s", head, typeString(target, qualifier))}
}

func definedTypeLine(head string, underlying types.Type, qualifier types.Qualifier) string {
	switch t := underlying.(type) {
	case *types.Struct:
		fields := exportedStructFields(t, qualifier)
		if len(fields) == 0 {
			return fmt.Sprintf("type %s struct{}", head)
		}
		return fmt.Sprintf("type %s struct{ %s }", head, strings.Join(fields, "; "))
	case *types.Interface:
		methods := exportedInterfaceMethods(t, qualifier)
		if len(methods) == 0 {
			return fmt.Sprintf("type %s interface{}", head)
		}
		return fmt.Sprintf("type %s interface{ %s }", head, strings.Join(methods, "; "))
	default:
		return fmt.Sprintf("type %s %s", head, typeString(underlying, qualifier))
	}
}

func exportedStructFields(strct *types.Struct, qualifier types.Qualifier) []string {
	fields := make([]string, 0, strct.NumFields())
	for i := 0; i < strct.NumFields(); i++ {
		field := strct.Field(i)
		if !field.Exported() {
			continue
		}
		name := field.Name()
		if field.Embedded() {
			name = typeString(field.Type(), qualifier)
		}
		fields = append(fields, fmt.Sprintf("%s %s", name, typeString(field.Type(), qualifier)))
	}
	sort.Strings(fields)
	return fields
}

func exportedInterfaceMethods(iface *types.Interface, qualifier types.Qualifier) []string {
	iface.Complete()
	methods := make([]string, 0, iface.NumMethods())
	for i := 0; i < iface.NumMethods(); i++ {
		method := iface.Method(i)
		if !method.Exported() {
			continue
		}
		methods = append(methods, fmt.Sprintf("%s%s", method.Name(), signatureTail(method.Type().(*types.Signature), qualifier)))
	}
	sort.Strings(methods)
	return methods
}

func methodLines(named *types.Named, qualifier types.Qualifier) []string {
	seen := map[string]struct{}{}
	lines := []string{}
	collect := func(set *types.MethodSet) {
		for i := 0; i < set.Len(); i++ {
			method, ok := set.At(i).Obj().(*types.Func)
			if !ok || !method.Exported() {
				continue
			}
			sig := method.Type().(*types.Signature)
			recv := sig.Recv()
			if recv == nil {
				continue
			}
			line := fmt.Sprintf("method (%s) %s%s", typeString(recv.Type(), qualifier), method.Name(), signatureTail(sig, qualifier))
			if _, ok := seen[line]; ok {
				continue
			}
			seen[line] = struct{}{}
			lines = append(lines, line)
		}
	}
	collect(types.NewMethodSet(named))
	collect(types.NewMethodSet(types.NewPointer(named)))
	sort.Strings(lines)
	return lines
}

func signatureTail(sig *types.Signature, qualifier types.Qualifier) string {
	var b strings.Builder
	b.WriteString(typeParamList(sig.TypeParams(), qualifier))
	b.WriteString(tupleString(sig.Params(), sig.Variadic(), qualifier))
	results := sig.Results()
	switch results.Len() {
	case 0:
	case 1:
		result := results.At(0)
		if result.Name() == "" {
			b.WriteString(" ")
			b.WriteString(typeString(result.Type(), qualifier))
			break
		}
		fallthrough
	default:
		b.WriteString(" ")
		b.WriteString(tupleString(results, false, qualifier))
	}
	return b.String()
}

func tupleString(tuple *types.Tuple, variadic bool, qualifier types.Qualifier) string {
	if tuple == nil || tuple.Len() == 0 {
		return "()"
	}
	parts := make([]string, 0, tuple.Len())
	for i := 0; i < tuple.Len(); i++ {
		v := tuple.At(i)
		typ := typeString(v.Type(), qualifier)
		if variadic && i == tuple.Len()-1 {
			if slice, ok := v.Type().(*types.Slice); ok {
				typ = "..." + typeString(slice.Elem(), qualifier)
			}
		}
		if v.Name() != "" {
			typ = v.Name() + " " + typ
		}
		parts = append(parts, typ)
	}
	return "(" + strings.Join(parts, ", ") + ")"
}

func typeParamList(params *types.TypeParamList, qualifier types.Qualifier) string {
	if params == nil || params.Len() == 0 {
		return ""
	}
	items := make([]string, 0, params.Len())
	for i := 0; i < params.Len(); i++ {
		param := params.At(i)
		items = append(items, fmt.Sprintf("%s %s", param.Obj().Name(), typeString(param.Constraint(), qualifier)))
	}
	return "[" + strings.Join(items, ", ") + "]"
}

func qualifierFor(current *types.Package) types.Qualifier {
	return func(pkg *types.Package) string {
		if pkg == nil || pkg.Path() == current.Path() {
			return ""
		}
		return pkg.Path()
	}
}

func typeString(t types.Type, qualifier types.Qualifier) string {
	return types.TypeString(t, qualifier)
}

func constantString(v constant.Value) string {
	if v == nil {
		return "<nil>"
	}
	return v.ExactString()
}

func isPublicAPIPackage(importPath string) bool {
	if importPath == modulePath {
		return true
	}
	rest, ok := strings.CutPrefix(importPath, modulePath+"/")
	if !ok || strings.Contains(rest, "/") {
		return false
	}
	return strings.HasPrefix(rest, "v")
}

func fatal(err error) {
	_, _ = fmt.Fprintln(os.Stderr, err)
	os.Exit(1)
}
