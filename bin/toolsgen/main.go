// Command toolsgen generates docs/api/tools.json, a machine-readable catalog of
// the public v* facade functions for AI/tooling consumption. It is the JSON
// companion to docs/api/exports.txt and is CI-enforced for drift the same way.
//
// Output is fully deterministic: every collection is sorted so that re-running
// the generator on unchanged source produces byte-identical JSON.
package main

import (
	"encoding/json"
	"flag"
	"fmt"
	"go/ast"
	"go/types"
	"os"
	"sort"
	"strings"

	"golang.org/x/tools/go/packages"
)

const modulePath = "github.com/imajinyun/go-knifer"

// schemaVersion is bumped when the tools.json structure changes.
const schemaVersion = "1.3"

// ToolsDoc is the top-level machine-readable tool catalog.
type ToolsDoc struct {
	Schema   string       `json:"schema"`
	Module   string       `json:"module"`
	Summary  SummaryDoc   `json:"summary"`
	Packages []PackageDoc `json:"packages"`
}

// SummaryDoc stores precomputed catalog counts so AI consumers can understand
// the catalog without scanning every package and function first.
type SummaryDoc struct {
	PackageCount          int            `json:"package_count"`
	FunctionCount         int            `json:"function_count"`
	FunctionsWithExamples int            `json:"functions_with_examples"`
	ContextAwareFunctions int            `json:"context_aware_functions"`
	ReturnsErrorFunctions int            `json:"returns_error_functions"`
	VariadicFunctions     int            `json:"variadic_functions"`
	SynopsisSources       map[string]int `json:"synopsis_sources"`
}

// PackageDoc describes one public facade package.
type PackageDoc struct {
	ImportPath string    `json:"import_path"`
	Name       string    `json:"name"`
	Synopsis   string    `json:"synopsis"`
	Functions  []FuncDoc `json:"functions"`
}

// FuncDoc describes one exported top-level function.
type FuncDoc struct {
	Name           string   `json:"name"`
	Signature      string   `json:"signature"`
	Synopsis       string   `json:"synopsis"`
	SynopsisSource string   `json:"synopsis_source,omitempty"`
	Params         []Param  `json:"params,omitempty"`
	Results        []string `json:"results,omitempty"`
	ReturnsError   bool     `json:"returns_error"`
	ContextAware   bool     `json:"context_aware"`
	Variadic       bool     `json:"variadic"`
	Examples       []string `json:"examples,omitempty"`
}

// Param is a single function parameter.
type Param struct {
	Name string `json:"name"`
	Type string `json:"type"`
}

func main() {
	outPath := flag.String("out", "", "write generated catalog to this file instead of stdout")
	markdownPath := flag.String("markdown", "", "write a human-readable Markdown catalog to this file")
	quality := flag.Bool("quality", false, "write a Markdown quality report to stdout and exit")
	flag.Parse()

	doc, err := generateToolsDoc(".")
	if err != nil {
		fatal(err)
	}
	if *quality {
		if _, err := os.Stdout.Write(renderToolsQualityReport(doc)); err != nil {
			fatal(err)
		}
		return
	}
	if err := writeToolsDoc(doc, *outPath); err != nil {
		fatal(err)
	}
	if err := writeMarkdownDoc(doc, *markdownPath); err != nil {
		fatal(err)
	}
}

func writeToolsDoc(doc ToolsDoc, outPath string) error {
	out, err := json.MarshalIndent(doc, "", "  ")
	if err != nil {
		return err
	}
	out = append(out, '\n')
	if outPath != "" {
		// Generated repository docs should be readable like normal source files.
		//nolint:gosec
		return os.WriteFile(outPath, out, 0o644)
	}
	if _, err := os.Stdout.Write(out); err != nil {
		return err
	}
	return nil
}

func writeMarkdownDoc(doc ToolsDoc, outPath string) error {
	if outPath == "" {
		return nil
	}
	// Generated repository docs should be readable like normal source files.
	//nolint:gosec
	return os.WriteFile(outPath, renderToolsMarkdown(doc), 0o644)
}

func renderToolsMarkdown(doc ToolsDoc) []byte {
	var b strings.Builder
	b.WriteString("# go-knifer Machine-readable Tool Catalog\n\n")
	b.WriteString("<!-- Generated by `make docs-gen`; do not edit by hand. -->\n\n")
	b.WriteString("This document is generated from `docs/api/tools.json` for human review and AI retrieval. ")
	b.WriteString("Regenerate it with `make docs-gen` after intentional facade, doc comment, or Example changes.\n\n")

	b.WriteString("## Summary\n\n")
	b.WriteString("| Metric | Value |\n")
	b.WriteString("| --- | ---: |\n")
	writeMetric(&b, "Schema", doc.Schema)
	writeMetric(&b, "Module", "`"+doc.Module+"`")
	writeMetric(&b, "Packages", doc.Summary.PackageCount)
	writeMetric(&b, "Functions", doc.Summary.FunctionCount)
	writeMetric(&b, "Functions with examples", doc.Summary.FunctionsWithExamples)
	writeMetric(&b, "Context-aware functions", doc.Summary.ContextAwareFunctions)
	writeMetric(&b, "Functions returning error", doc.Summary.ReturnsErrorFunctions)
	writeMetric(&b, "Variadic functions", doc.Summary.VariadicFunctions)
	for _, source := range orderedSynopsisSources(doc.Summary.SynopsisSources) {
		writeMetric(&b, "Synopsis source: "+source, doc.Summary.SynopsisSources[source])
	}

	b.WriteString("\n## Packages\n\n")
	for _, pkg := range doc.Packages {
		b.WriteString("### ")
		b.WriteString(pkg.Name)
		b.WriteString("\n\n")
		b.WriteString("Import path: `")
		b.WriteString(pkg.ImportPath)
		b.WriteString("`\n\n")
		if pkg.Synopsis != "" {
			b.WriteString(pkg.Synopsis)
			b.WriteString("\n\n")
		}
		b.WriteString("| Function | Signature | Synopsis | Source | Examples |\n")
		b.WriteString("| --- | --- | --- | --- | --- |\n")
		for _, fn := range pkg.Functions {
			b.WriteString("| `")
			b.WriteString(fn.Name)
			b.WriteString("` | `")
			b.WriteString(markdownEscape(fn.Signature))
			b.WriteString("` | ")
			b.WriteString(markdownText(fn.Synopsis))
			b.WriteString(" | ")
			b.WriteString(markdownText(synopsisSource(fn.SynopsisSource)))
			b.WriteString(" | ")
			b.WriteString(markdownExamples(fn.Examples))
			b.WriteString(" |\n")
		}
		b.WriteString("\n")
	}
	return []byte(b.String())
}

type packageQualityRow struct {
	Name                  string
	FunctionCount         int
	EmptySynopses         int
	FunctionsWithDocs     int
	FunctionsWithExamples int
	EmptyFunctionNames    []string
}

func buildPackageQualityRows(doc ToolsDoc) []packageQualityRow {
	rows := make([]packageQualityRow, 0, len(doc.Packages))
	for _, pkg := range doc.Packages {
		row := packageQualityRow{Name: pkg.Name, FunctionCount: len(pkg.Functions)}
		for _, fn := range pkg.Functions {
			if len(fn.Examples) > 0 {
				row.FunctionsWithExamples++
			}
			if strings.TrimSpace(fn.Synopsis) == "" {
				row.EmptySynopses++
				row.EmptyFunctionNames = append(row.EmptyFunctionNames, fn.Name)
				continue
			}
			row.FunctionsWithDocs++
		}
		rows = append(rows, row)
	}
	sort.Slice(rows, func(i, j int) bool {
		if rows[i].EmptySynopses != rows[j].EmptySynopses {
			return rows[i].EmptySynopses > rows[j].EmptySynopses
		}
		return rows[i].Name < rows[j].Name
	})
	return rows
}

func renderToolsQualityReport(doc ToolsDoc) []byte {
	var b strings.Builder
	b.WriteString("# go-knifer Tool Catalog Quality Report\n\n")
	b.WriteString("Generated from public facade source comments and examples. Use `make tools-report` to refresh this diagnostic view.\n\n")
	b.WriteString("## Summary\n\n")
	b.WriteString("| Metric | Value |\n")
	b.WriteString("| --- | ---: |\n")
	writeMetric(&b, "Packages", doc.Summary.PackageCount)
	writeMetric(&b, "Functions", doc.Summary.FunctionCount)
	writeMetric(&b, "Empty synopses", doc.Summary.SynopsisSources["empty"])
	writeMetric(&b, "Functions with examples", doc.Summary.FunctionsWithExamples)

	b.WriteString("\n## Package ranking\n\n")
	b.WriteString("| Package | Functions | Empty synopses | With docs | With examples | Empty functions |\n")
	b.WriteString("| --- | ---: | ---: | ---: | ---: | --- |\n")
	for _, row := range buildPackageQualityRows(doc) {
		fmt.Fprintf(
			&b,
			"| `%s` | %d | %d | %d | %d | %s |\n",
			markdownEscape(row.Name),
			row.FunctionCount,
			row.EmptySynopses,
			row.FunctionsWithDocs,
			row.FunctionsWithExamples,
			markdownFunctionList(row.EmptyFunctionNames),
		)
	}
	return []byte(b.String())
}

func markdownFunctionList(names []string) string {
	if len(names) == 0 {
		return "—"
	}
	parts := make([]string, 0, len(names))
	for _, name := range names {
		parts = append(parts, "`"+markdownEscape(name)+"`")
	}
	return strings.Join(parts, ", ")
}

func writeMetric(b *strings.Builder, name string, value any) {
	fmt.Fprintf(b, "| %s | %v |\n", markdownText(name), value)
}

func orderedSynopsisSources(sources map[string]int) []string {
	preferred := []string{"facade", "internal", "empty"}
	seen := map[string]struct{}{}
	out := make([]string, 0, len(sources))
	for _, source := range preferred {
		if _, ok := sources[source]; ok {
			out = append(out, source)
			seen[source] = struct{}{}
		}
	}
	var extra []string
	for source := range sources {
		if _, ok := seen[source]; !ok {
			extra = append(extra, source)
		}
	}
	sort.Strings(extra)
	return append(out, extra...)
}

func markdownExamples(examples []string) string {
	if len(examples) == 0 {
		return "—"
	}
	parts := make([]string, 0, len(examples))
	for _, example := range examples {
		parts = append(parts, "`"+markdownEscape(example)+"`")
	}
	return strings.Join(parts, ", ")
}

func markdownText(text string) string {
	if strings.TrimSpace(text) == "" {
		return "—"
	}
	return markdownEscape(text)
}

func markdownEscape(text string) string {
	replacer := strings.NewReplacer(
		"\\", "\\\\",
		"|", "\\|",
		"\r", " ",
		"\n", " ",
	)
	return replacer.Replace(text)
}

func synopsisSource(source string) string {
	if source == "" {
		return "empty"
	}
	return source
}

func generateToolsDoc(root string) (ToolsDoc, error) {
	cfg := &packages.Config{
		Dir:   root,
		Tests: true,
		Mode: packages.NeedName | packages.NeedTypes | packages.NeedTypesInfo |
			packages.NeedSyntax | packages.NeedFiles | packages.NeedImports | packages.NeedDeps,
	}
	pkgs, err := packages.Load(cfg, "./...")
	if err != nil {
		return ToolsDoc{}, err
	}

	// examples maps facade import path -> set of Example function names.
	examples := map[string]map[string]struct{}{}
	// implDocs maps fully-qualified function names to their first-sentence docs.
	implDocs := map[string]string{}
	// main packages keyed by import path (deduplicated across test variants).
	mains := map[string]*packages.Package{}

	for _, pkg := range pkgs {
		if len(pkg.Errors) > 0 {
			return ToolsDoc{}, pkg.Errors[0]
		}
		collectExamples(pkg, examples)
		collectFunctionDocs(pkg, implDocs)
		facade := strings.TrimSuffix(pkg.PkgPath, "_test")
		if pkg.Types == nil || !isFacadePackage(facade) {
			continue
		}
		// Prefer the non-test build of the package (it carries only the real scope).
		if pkg.PkgPath == facade && !hasTestFiles(pkg) {
			mains[facade] = pkg
		}
	}

	out := ToolsDoc{Schema: schemaVersion, Module: modulePath}
	for path, pkg := range mains {
		pd := buildPackageDoc(pkg, examples[path], implDocs)
		if len(pd.Functions) == 0 {
			continue
		}
		out.Packages = append(out.Packages, pd)
	}
	sort.Slice(out.Packages, func(i, j int) bool {
		return out.Packages[i].ImportPath < out.Packages[j].ImportPath
	})
	out.Summary = summarizeToolsDoc(out.Packages)
	return out, nil
}

func summarizeToolsDoc(pkgs []PackageDoc) SummaryDoc {
	summary := SummaryDoc{
		PackageCount:    len(pkgs),
		SynopsisSources: map[string]int{"empty": 0, "facade": 0, "internal": 0},
	}
	for _, pkg := range pkgs {
		for _, fn := range pkg.Functions {
			summary.FunctionCount++
			if len(fn.Examples) > 0 {
				summary.FunctionsWithExamples++
			}
			if fn.ContextAware {
				summary.ContextAwareFunctions++
			}
			if fn.ReturnsError {
				summary.ReturnsErrorFunctions++
			}
			if fn.Variadic {
				summary.VariadicFunctions++
			}

			source := fn.SynopsisSource
			if source == "" {
				source = "empty"
			}
			summary.SynopsisSources[source]++
		}
	}
	return summary
}

func hasTestFiles(pkg *packages.Package) bool {
	for _, file := range pkg.GoFiles {
		if strings.HasSuffix(file, "_test.go") {
			return true
		}
	}
	return false
}

// collectFunctionDocs records first-sentence docs for functions and methods in
// all loaded packages. Facade functions without comments can reuse the
// documentation of the internal function or method they directly delegate to.
func collectFunctionDocs(pkg *packages.Package, docs map[string]string) {
	if pkg.Types == nil || hasTestFiles(pkg) {
		return
	}
	for _, file := range pkg.Syntax {
		for _, decl := range file.Decls {
			fn, ok := decl.(*ast.FuncDecl)
			if !ok || fn.Name == nil || fn.Doc == nil {
				continue
			}
			obj, _ := pkg.TypesInfo.Defs[fn.Name].(*types.Func)
			if obj == nil {
				continue
			}
			docs[functionKey(obj)] = firstSentence(fn.Doc.Text())
		}
	}
}

// collectExamples records top-level ExampleXxx functions, attributing them to
// the facade package they test.
func collectExamples(pkg *packages.Package, examples map[string]map[string]struct{}) {
	facade := strings.TrimSuffix(pkg.PkgPath, "_test")
	if !isFacadePackage(facade) {
		return
	}
	for _, file := range pkg.Syntax {
		for _, decl := range file.Decls {
			fn, ok := decl.(*ast.FuncDecl)
			if !ok || fn.Recv != nil || fn.Name == nil {
				continue
			}
			if !strings.HasPrefix(fn.Name.Name, "Example") {
				continue
			}
			if examples[facade] == nil {
				examples[facade] = map[string]struct{}{}
			}
			examples[facade][fn.Name.Name] = struct{}{}
		}
	}
}

func buildPackageDoc(pkg *packages.Package, exampleSet map[string]struct{}, implDocs map[string]string) PackageDoc {
	pd := PackageDoc{
		ImportPath: pkg.PkgPath,
		Name:       pkg.Name,
		Synopsis:   packageSynopsis(pkg),
	}
	docs := funcDocComments(pkg, implDocs)
	qualifier := qualifierFor(pkg.Types)
	scope := pkg.Types.Scope()
	for _, name := range scope.Names() {
		obj, ok := scope.Lookup(name).(*types.Func)
		if !ok || !obj.Exported() {
			continue
		}
		sig, ok := obj.Type().(*types.Signature)
		if !ok || sig.Recv() != nil {
			continue
		}
		pd.Functions = append(pd.Functions, buildFuncDoc(obj, sig, qualifier, docs[name], exampleSet))
	}
	sort.Slice(pd.Functions, func(i, j int) bool {
		return pd.Functions[i].Name < pd.Functions[j].Name
	})
	return pd
}

func buildFuncDoc(obj *types.Func, sig *types.Signature, qualifier types.Qualifier, doc functionDoc, exampleSet map[string]struct{}) FuncDoc {
	fd := FuncDoc{
		Name:           obj.Name(),
		Signature:      "func " + obj.Name() + signatureTail(sig, qualifier),
		Synopsis:       doc.synopsis,
		SynopsisSource: doc.source,
		Variadic:       sig.Variadic(),
	}

	params := sig.Params()
	for i := 0; i < params.Len(); i++ {
		v := params.At(i)
		typ := typeString(v.Type(), qualifier)
		if sig.Variadic() && i == params.Len()-1 {
			if slice, ok := v.Type().(*types.Slice); ok {
				typ = "..." + typeString(slice.Elem(), qualifier)
			}
		}
		fd.Params = append(fd.Params, Param{Name: v.Name(), Type: typ})
		if i == 0 && typ == "context.Context" {
			fd.ContextAware = true
		}
	}

	results := sig.Results()
	for i := 0; i < results.Len(); i++ {
		typ := typeString(results.At(i).Type(), qualifier)
		fd.Results = append(fd.Results, typ)
		if typ == "error" {
			fd.ReturnsError = true
		}
	}

	fd.Examples = matchExamples(obj.Name(), exampleSet)
	return fd
}

// matchExamples returns sorted Example function names that document fn.
// Go test convention: ExampleFn or ExampleFn_suffix documents function Fn.
func matchExamples(fnName string, exampleSet map[string]struct{}) []string {
	if len(exampleSet) == 0 {
		return nil
	}
	var out []string
	for name := range exampleSet {
		rest, ok := strings.CutPrefix(name, "Example")
		if !ok {
			continue
		}
		base, _, _ := strings.Cut(rest, "_")
		if base == fnName {
			out = append(out, name)
		}
	}
	sort.Strings(out)
	return out
}

type functionDoc struct {
	synopsis string
	source   string
}

// funcDocComments returns the first-sentence doc comment for each top-level
// facade func. If a facade has no comment but directly delegates to an internal
// function with a comment, the internal comment is used as a fallback.
func funcDocComments(pkg *packages.Package, implDocs map[string]string) map[string]functionDoc {
	docs := map[string]functionDoc{}
	for _, file := range pkg.Syntax {
		for _, decl := range file.Decls {
			fn, ok := decl.(*ast.FuncDecl)
			if !ok || fn.Recv != nil || fn.Name == nil {
				continue
			}
			if fn.Doc != nil {
				docs[fn.Name.Name] = functionDoc{synopsis: firstSentence(fn.Doc.Text()), source: "facade"}
				continue
			}
			if delegated := delegatedFunc(fn, pkg); delegated != nil {
				if synopsis := implDocs[functionKey(delegated)]; synopsis != "" {
					docs[fn.Name.Name] = functionDoc{synopsis: synopsis, source: "internal"}
				}
			}
		}
	}
	return docs
}

func delegatedFunc(fn *ast.FuncDecl, pkg *packages.Package) *types.Func {
	if fn.Body == nil || len(fn.Body.List) != 1 {
		return nil
	}
	var call *ast.CallExpr
	switch stmt := fn.Body.List[0].(type) {
	case *ast.ReturnStmt:
		if len(stmt.Results) != 1 {
			return nil
		}
		call, _ = stmt.Results[0].(*ast.CallExpr)
	case *ast.ExprStmt:
		call, _ = stmt.X.(*ast.CallExpr)
	}
	if call == nil {
		return nil
	}
	if pkg.TypesInfo == nil {
		return nil
	}
	selector, ok := call.Fun.(*ast.SelectorExpr)
	if !ok || selector.Sel == nil {
		return nil
	}
	if obj, ok := pkg.TypesInfo.Uses[selector.Sel].(*types.Func); ok && obj.Pkg() != nil {
		return obj
	}

	// Facade helpers sometimes delegate through a method on an aliased internal
	// type, for example return b.AddRootCABytes(pem). Selector Uses is nil for
	// methods, but Selections points at the target method.
	selection := pkg.TypesInfo.Selections[selector]
	if selection == nil || selection.Kind() != types.MethodVal {
		return nil
	}
	obj, _ := selection.Obj().(*types.Func)
	if obj == nil || obj.Pkg() == nil {
		return nil
	}
	return obj
}

func functionKey(fn *types.Func) string {
	if fn == nil {
		return ""
	}
	return fn.FullName()
}

func packageSynopsis(pkg *packages.Package) string {
	for _, file := range pkg.Syntax {
		if file.Doc != nil {
			return firstSentence(file.Doc.Text())
		}
	}
	return ""
}

// firstSentence returns the first sentence of a doc comment, collapsed to a
// single line.
func firstSentence(text string) string {
	text = strings.TrimSpace(text)
	if text == "" {
		return ""
	}
	if idx := strings.Index(text, "\n\n"); idx >= 0 {
		text = text[:idx]
	}
	text = strings.Join(strings.Fields(text), " ")
	// Trim to the first sentence-ending period followed by a space or EOS.
	for i := 0; i < len(text); i++ {
		if text[i] == '.' && (i+1 == len(text) || text[i+1] == ' ') {
			return text[:i+1]
		}
	}
	return text
}

func signatureTail(sig *types.Signature, qualifier types.Qualifier) string {
	var b strings.Builder
	b.WriteString(typeParamList(sig.TypeParams(), qualifier))
	b.WriteString(tupleString(sig.Params(), sig.Variadic(), qualifier))
	results := sig.Results()
	switch results.Len() {
	case 0:
	case 1:
		result := results.At(0)
		if result.Name() == "" {
			b.WriteString(" ")
			b.WriteString(typeString(result.Type(), qualifier))
			break
		}
		fallthrough
	default:
		b.WriteString(" ")
		b.WriteString(tupleString(results, false, qualifier))
	}
	return b.String()
}

func tupleString(tuple *types.Tuple, variadic bool, qualifier types.Qualifier) string {
	if tuple == nil || tuple.Len() == 0 {
		return "()"
	}
	parts := make([]string, 0, tuple.Len())
	for i := 0; i < tuple.Len(); i++ {
		v := tuple.At(i)
		typ := typeString(v.Type(), qualifier)
		if variadic && i == tuple.Len()-1 {
			if slice, ok := v.Type().(*types.Slice); ok {
				typ = "..." + typeString(slice.Elem(), qualifier)
			}
		}
		if v.Name() != "" {
			typ = v.Name() + " " + typ
		}
		parts = append(parts, typ)
	}
	return "(" + strings.Join(parts, ", ") + ")"
}

func typeParamList(params *types.TypeParamList, qualifier types.Qualifier) string {
	if params == nil || params.Len() == 0 {
		return ""
	}
	items := make([]string, 0, params.Len())
	for i := 0; i < params.Len(); i++ {
		param := params.At(i)
		items = append(items, fmt.Sprintf("%s %s", param.Obj().Name(), typeString(param.Constraint(), qualifier)))
	}
	return "[" + strings.Join(items, ", ") + "]"
}

func qualifierFor(current *types.Package) types.Qualifier {
	return func(pkg *types.Package) string {
		if pkg == nil || pkg.Path() == current.Path() {
			return ""
		}
		// Use the short package name to keep signatures readable.
		return pkg.Name()
	}
}

func typeString(t types.Type, qualifier types.Qualifier) string {
	return types.TypeString(t, qualifier)
}

func isFacadePackage(importPath string) bool {
	rest, ok := strings.CutPrefix(importPath, modulePath+"/")
	if !ok || strings.Contains(rest, "/") {
		return false
	}
	return strings.HasPrefix(rest, "v")
}

func fatal(err error) {
	_, _ = fmt.Fprintln(os.Stderr, err)
	os.Exit(1)
}
